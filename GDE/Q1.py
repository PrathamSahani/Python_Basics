# Function initialization
def greet(name):
    print(f"Hello, {name}!")

# The function 'greet' is initialized and can now be called.


# Function definition
def add(a, b):
    result = a + b
    return result

# The 'add' function is defined to add two numbers and return the result.


# Function call
greet("Alice")
sum_result = add(5, 3)

# The 'greet' function is called with the argument "Alice."
# The 'add' function is called with arguments 5 and 3, and its result is assigned to 'sum_result.'

