# Creating a set
my_set = {1, 2, 3, 3, 4, 5}  # Duplicate values are automatically removed
print(my_set)  # Output: {1, 2, 3, 4, 5}

# Set operations
set1 = {1, 2, 3}
set2 = {3, 4, 5}
union_set = set1.union(set2)  # Union of two sets
intersection_set = set1.intersection(set2)  # Intersection of two sets
difference_set = set1.difference(set2)  # Set difference


# Creating a list
my_list = [1, 2, 3, 3, 4, 5]
print(my_list)  # Output: [1, 2, 3, 3, 4, 5]

# Accessing elements by index
element = my_list[2]  # Access the third element (index 2)



# Creating a dictionary
my_dict = {"name": "John", "age": 30, "city": "New York"}
print(my_dict)  # Output: {'name': 'John', 'age': 30, 'city': 'New York'}

# Accessing values by keys
name = my_dict["name"]  # Access the value associated with the "name" key
